'''In Python, a variable is a named reference to a value stored in the computer's memory. 
It acts as a container to hold data, allowing you to manipulate and access it throughout your program.
To declare and assign a value to a variable in Python, you can follow these steps:
Declaration: Variables in Python do not need explicit declaration before assigning a value. You can 
directly start using a variable name to declare it. For example:'''

name = "GAURAV"
'''In this example, we declare a variable named name and assign the value "GAURAV" to it. Python recognizes the variable 
name as a new identifier and allocates memory to store the value.'''

'''Assignment: Assigning a value to a variable is done using the assignment operator =. The value on the right side of the = operator 
is assigned to the variable on the left side. For example:'''
age = 24
'''In this case, we assign the value 24 to the variable age.'''

